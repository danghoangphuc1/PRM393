  static List<LearningItem> _build(String categoryId, List<WordTriple> words) {
    return List.generate(words.length, (i) {
      final w = words[i];
      return LearningItem(
        id: '${categoryId}_$i',
        categoryId: categoryId,
        word: w.$1,
        emoji: w.$2,
        meaning: w.$3,
      );
    });
  }

  static List<LearningItem> getItems(String categoryId) =>
      itemsByCategory[categoryId] ?? [];

  static List<LearningItem> allItems() =>
      itemsByCategory.values.expand((list) => list).toList();
